  }

  /// 为滤镜提示词添加统一的保护指令（参考_addCompositionProtection的模式）
  static String _addFilterProtectionInstructions(String basePrompt) {
    const filterProtection = '''
🎨 【滤镜处理核心保护 - 最高优先级】：
1. ⚠️ CRITICAL: 严格保持原图构图，主体人物位置完全不变
2. ⚠️ CRITICAL: 保持原有画面比例和布局，禁止裁剪或移动  
3. ⚠️ CRITICAL: 滤镜效果要自然流畅，避免过度处理
4. ⚠️ CRITICAL: 保持面部特征和身体比例真实性
5. ⚠️ CRITICAL: 输出高质量4K分辨率图片

''';

    const qualityStandard = '''
🎯 【滤镜质量标准】：
- 滤镜效果自然流畅，无明显瑕疵或失真
- 保持原图清晰度和重要细节
- 确保色彩过渡自然，避免突兀变化
- 主体位置和构图的保持是最高优先级
''';

    return _insertFilterInstructions(
      basePrompt,
      filterProtection,
      qualityStandard,
    );
  }

  /// 为滤镜预览添加快速处理指令
  static String _addFilterPreviewInstructions(String basePrompt) {
    const previewInstructions = '''
👀 【预览模式 - 快速处理】：
1. 保持构图和位置不变
2. 应用滤镜效果但优化处理速度
3. 输出预览质量即可，无需4K
4. 重点展示滤镜的视觉效果

''';

    return previewInstructions + basePrompt + '\n请返回预览效果图片。';
  }

  /// 滤镜指令插入方法（类似现有的插入逻辑）
  static String _insertFilterInstructions(
    String basePrompt,
    String filterProtection,
    String qualityStandard,
  ) {
    final lines = basePrompt.split('\n');
    final processedLines = <String>[];

    bool hasAddedProtection = false;
    for (int i = 0; i < lines.length; i++) {
      final line = lines[i].trim();

      // 在第一行后插入滤镜保护指令
      if (!hasAddedProtection && i == 0) {
        processedLines.add(lines[i]);
        processedLines.add(filterProtection);
        hasAddedProtection = true;
        continue;
      }

      // 在"请返回"前插入质量标准
      if (line.startsWith('请返回')) {
        processedLines.add(qualityStandard);
      }

      processedLines.add(lines[i]);
    }

    return processedLines.join('\n');
  }

  /// 获取基础滤镜提示词
